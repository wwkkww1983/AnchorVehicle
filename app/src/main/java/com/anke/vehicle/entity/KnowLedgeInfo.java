package com.anke.vehicle.entity;

public class KnowLedgeInfo
{
	private int Id;
	private String Name;
	private String Pyt;
	public int getId()
	{
		return Id;
	}
	public void setId(int id)
	{
		Id = id;
	}
	public String getName()
	{
		return Name;
	}
	public void setName(String name)
	{
		Name = name;
	}
	public String getPyt()
	{
		return Pyt;
	}
	public void setPyt(String pyt)
	{
		Pyt = pyt;
	}
	
	
}
