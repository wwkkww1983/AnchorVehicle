package com.anke.vehicle.entity;


public class GPSParamInfo
{
	private String gps;
	private String gpsnotask;
	private String gpstask;
	public String getGps()
	{
		return gps;
	}
	public void setGps(String gps)
	{
		this.gps = gps;
	}
	public String getGpsnotask()
	{
		return gpsnotask;
	}
	public void setGpsnotask(String gpsnotask)
	{
		this.gpsnotask = gpsnotask;
	}
	public String getGpstask()
	{
		return gpstask;
	}
	public void setGpstask(String gpstask)
	{
		this.gpstask = gpstask;
	}
}
