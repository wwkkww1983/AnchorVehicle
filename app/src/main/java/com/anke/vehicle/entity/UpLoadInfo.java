package com.anke.vehicle.entity;

public class UpLoadInfo
{
	private String TaskCoding;
	private String AmbCoding;
	private String Hospital;
	private String Judge;
	private String Ill;
	public String getTaskCoding()
	{
		return TaskCoding;
	}
	public void setTaskCoding(String taskCoding)
	{
		TaskCoding = taskCoding;
	}
	public String getAmbCoding()
	{
		return AmbCoding;
	}
	public void setAmbCoding(String ambCoding)
	{
		AmbCoding = ambCoding;
	}
	public String getJudge()
	{
		return Judge;
	}
	public void setJudge(String judge)
	{
		Judge = judge;
	}
	public String getHospital()
	{
		return Hospital;
	}
	public void setHospital(String hospital)
	{
		Hospital = hospital;
	}
	public String getIll()
	{
		return Ill;
	}
	public void setIll(String ill)
	{
		Ill = ill;
	}
	
}
